    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        // TODO add your handling code here:
        try
        {
            ArrayList<Employee>empList = EmpDAO.getAllEmployee();
            if(empList.size() == 0)
            {
                JOptionPane.showMessageDialog(null, "No record found in the database", "Empty Database", JOptionPane.INFORMATION_MESSAGE);
                return ;
            }
            StringBuffer sb = new StringBuffer();
            sb.append("EmpNo\tEmpName\tEmpSal\n\n");
            for(Employee e : empList)
            {
                sb.append(e.getEmpNo() + "\t" + e.getEmpName() + "\t" + e.getEmpSal() + "\n");
            }
            txtArea.setText(sb.toString());
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Database Error.", "ERROR", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnShowActionPerformed

